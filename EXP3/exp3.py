s = "Python Programming"
print("\nString Operations:\n")
#length
print("1. Length:", len(s))

#capitalize
print("2. Capitalize:", s.capitalize())

# Casefold
print("3. Casefold:", s.casefold())

#Starts with
print("4. Starts with: ", s.startswith(" "))

#Split
print("5. Split:", s.split())

#Replace
print("6. Replace:", s.replace("world", "Python"))

#lowercase
print("7. Lower:", s.lower())

#uppercase
print("8. Upper:", s.upper())

#Title
print("9. Title:", s.title())

#Swapcase
print("10. Swapcase:", s.swapcase())
